    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        if (textNome.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Obrigat√≥rio");
            textNome.requestFocus();
            return;
        }        
        
        Estaticas.Listas.getListaInstrutor().add(new Instrutor(textNome.getText(), (Sexos)comboSexo.getSelectedItem(), (Modalidade)comboModalidade.getSelectedItem()));
        
        textNome.setText("");
        comboSexo.setSelectedIndex(0);
        comboModalidade.setSelectedIndex(0);
        textNome.requestFocus();
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        // TODO add your handling code here:
        areaTexto.setText("Nome\tModalidade\n");
        
        for (Instrutor instrutor : Listas.getListaInstrutor()) {
            areaTexto.append(instrutor.getNome() +"\t"+ instrutor.getModalidade() +"\n");
        }
    }//GEN-LAST:event_btnListarActionPerformed

